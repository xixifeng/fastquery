package org.fastquery.mapper;

/**
 * 
 * @author xixifeng (fastquery@126.com)
 */
class QueryMapper {

	private final String id; // 重写id的hashCode和equals,用它作为唯一标识
	private final String template; // 模板

	QueryMapper(String id, String template) {
		this.id = id;
		this.template = template;
	}

	String getId() {
		return id;
	}

	String getTemplate() {
		return template;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		QueryMapper other = (QueryMapper) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id)) {
			return false;
		}
		return true;
	}

}
